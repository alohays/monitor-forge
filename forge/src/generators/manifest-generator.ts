function generatePanelManifest(config: MonitorForgeConfig): string {
  const panelTypes = new Set(config.panels.map(p => p.type));
  const imports: string[] = [
    "import { registerPanelType } from '../core/panels/panel-registry.js';",
    "import type { PanelConfig } from '../core/panels/PanelBase.js';",
  ];

  const typeMap: Record<string, string> = {
    'ai-brief': 'AIBriefPanel',
    'news-feed': 'NewsFeedPanel',
    'market-ticker': 'MarketTickerPanel',
    'entity-tracker': 'EntityTrackerPanel',
    'instability-index': 'InstabilityIndexPanel',
    'service-status': 'ServiceStatusPanel',
  };

  for (const type of panelTypes) {
    if (type !== 'custom' && typeMap[type]) {
      imports.push(`import { ${typeMap[type]} } from '../core/panels/types/${typeMap[type]}.js';`);
    }
  }

  const registrations: string[] = [];
  for (const type of panelTypes) {
    if (type !== 'custom' && typeMap[type]) {
      registrations.push(`registerPanelType('${type}', ${typeMap[type]});`);
    }
  }

  // Custom panels — import from src/custom-panels/ and register by panel name
  const customPanels = config.panels.filter(p => p.type === 'custom' && p.customModule);
  const seenModules = new Set<string>();
  for (const panel of customPanels) {
    const mod = panel.customModule!;
    if (!seenModules.has(mod)) {
      seenModules.add(mod);
      imports.push(`import { ${mod} } from '../custom-panels/${mod}.js';`);
    }
    registrations.push(`registerPanelType('${panel.name}', ${mod});`);
  }

  return `// Auto-generated by forge build. Do not edit.
${imports.join('\n')}

${registrations.join('\n')}

export const panelConfigs: PanelConfig[] = ${JSON.stringify(config.panels, null, 2)};
`;
}

export function generateProxyConfig(config: MonitorForgeConfig): string {
  const proxyEntries: Record<string, { envVar: string; header: string; scheme: 'bearer' | 'plain' }> = {};
  const ttlEntries: Record<string, number> = {};

  for (const source of config.sources) {
    // Build TTL map for all sources
    ttlEntries[source.name] = source.cacheTtl;

    // Build proxy auth config only for sources with authEnvVar
    if (source.authEnvVar) {
      try {
        const domain = new URL(source.url).hostname;
        const scheme = source.authHeader === 'Authorization' ? 'bearer' : 'plain';
        proxyEntries[domain] = {
          envVar: source.authEnvVar,
          header: source.authHeader,
          scheme,
        };
      } catch {
        // Skip invalid URLs (caught by schema validation)
      }
    }
  }

  return `// Auto-generated by forge build. Do not edit.
import type { ProxyAuthConfig } from './key-injection.js';

export const proxyConfig: Record<string, ProxyAuthConfig> = ${JSON.stringify(proxyEntries, null, 2)};

export const sourceTtl: Record<string, number> = ${JSON.stringify(ttlEntries, null, 2)};
`;
}

export function generateProxyAllowlist(config: MonitorForgeConfig): string {
  const domains = new Set<string>();

  // Extract hostnames from source URLs
  for (const source of config.sources) {
    try {
      domains.add(new URL(source.url).hostname);
    } catch {
      // Skip invalid URLs (caught by schema validation)
    }
  }

  // Extract hostnames from layer API URLs
  for (const layer of config.layers) {
    if (layer.data.source === 'api' && layer.data.url) {
      try {
        domains.add(new URL(layer.data.url).hostname);
      } catch {
        // Skip
      }
    }
  }

  // Add domains from proxy-required API templates
  try {
    const __filename = fileURLToPath(import.meta.url);
    const __dirname = dirname(__filename);
    const templatesPath = resolve(__dirname, '../../data/api-templates.json');
    const templates: ApiTemplates = JSON.parse(readFileSync(templatesPath, 'utf-8'));
    for (const template of templates.templates) {
      if (template.proxyRequired) {
        try {
          domains.add(new URL(template.baseUrl).hostname);
        } catch {
          // Skip invalid URLs
        }
      }
    }
  } catch {
    // API templates file not found — skip silently
  }

  // Merge explicitly configured allowedDomains (keep wildcard — isDomainAllowed handles it)
  for (const d of config.backend.corsProxy.allowedDomains) {
    domains.add(d);
  }

  const domainArray = Array.from(domains).sort();

  return `// Auto-generated by forge build. Do not edit.
export const PROXY_ALLOWED_DOMAINS: readonly string[] = ${JSON.stringify(domainArray, null, 2)};
export const CORS_ALLOWED_ORIGINS: readonly string[] = ${JSON.stringify(config.backend.corsProxy.corsOrigins, null, 2)};
`;
}

