export function generateManifests(config: MonitorForgeConfig): Record<string, string> {
  return {
    'source-manifest.ts': generateSourceManifest(config),
    'layer-manifest.ts': generateLayerManifest(config),
    'panel-manifest.ts': generatePanelManifest(config),
    'config-resolved.ts': generateResolvedConfig(config),
    'view-manifest.ts': generateViewManifest(config),
    'theme-resolved.ts': generateThemeManifest(config),
  };
}

function generateSourceManifest(config: MonitorForgeConfig): string {
  const imports = new Set<string>();
  const registrations: string[] = [];

  for (const source of config.sources) {
    switch (source.type) {
      case 'rss':
        imports.add("import { RSSSource } from '../core/sources/RSSSource.js';");
        break;
      case 'rest-api':
        imports.add("import { APISource } from '../core/sources/APISource.js';");
        break;
      case 'websocket':
        imports.add("import { WebSocketSource } from '../core/sources/WebSocketSource.js';");
        break;
    }
  }

  imports.add("import { registerSource } from '../core/sources/source-registry.js';");
  imports.add("import type { SourceConfig } from '../core/sources/SourceBase.js';");

  const sourceTypes = new Set(config.sources.map(s => s.type));
  if (sourceTypes.has('rss')) registrations.push("registerSource('rss', RSSSource);");
  if (sourceTypes.has('rest-api')) registrations.push("registerSource('rest-api', APISource);");
  if (sourceTypes.has('websocket')) registrations.push("registerSource('websocket', WebSocketSource);");

  return `// Auto-generated by forge build. Do not edit.
${Array.from(imports).join('\n')}

${registrations.join('\n')}

export const sourceConfigs: SourceConfig[] = ${JSON.stringify(config.sources.map(({ authEnvVar, ...rest }) => rest), null, 2)};
`;
}

